CREATE TABLE `pk_cashflow` (
  `CASHFLOW_ID` int(11) NOT NULL AUTO_INCREMENT,
  `USER_ID` int(11) NOT NULL,
  `TYPE` int(11) NOT NULL,
  `AMOUNT` decimal(15,2) NOT NULL,
  `PROMO` decimal(15,2) NOT NULL DEFAULT '0.00',
  `PROMO_SOURCE` int(11) DEFAULT NULL,
  `CASH_BALANCE` decimal(15,2) NOT NULL,
  `PRE_BALANCE` decimal(15,2) NOT NULL,
  `CURRENCY` char(16) NOT NULL DEFAULT '',
  `TAXES` decimal(15,2) NOT NULL,
  `SOURCE` int(11) DEFAULT NULL,
  `SOURCE_TYPE` int(11) DEFAULT NULL,
  `BILL_SERI` varchar(32) DEFAULT NULL,
  `BILL_NUMBER` varchar(32) DEFAULT NULL,
  `BILL_TYPE` varchar(16) DEFAULT NULL,
  `STATUS` int(11) NOT NULL DEFAULT '1',
  `INPUT_DATE` timestamp NOT NULL DEFAULT '1999-01-01 00:00:00',
  `UPDATE_DATE` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`CASHFLOW_ID`),
  KEY `INDEX_CF_USER_ID` (`USER_ID`) USING BTREE
) ENGINE=InnoDB AUTO_INCREMENT=317 DEFAULT CHARSET=utf8;

